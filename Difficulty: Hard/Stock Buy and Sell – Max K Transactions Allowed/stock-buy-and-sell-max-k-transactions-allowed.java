class Solution {
    static int maxProfit(int prices[], int k) {
        int n = prices.length;
        if (n == 0) return 0;
        
        // If k is greater than n/2, it's equivalent to unlimited transactions
        if (k >= n / 2) {
            int profit = 0;
            for (int i = 1; i < n; i++) {
                if (prices[i] > prices[i - 1]) {
                    profit += prices[i] - prices[i - 1];
                }
            }
            return profit;
        }
        
        // DP table
        int[][] dp = new int[k + 1][n];
        
        // Fill the DP table
        for (int t = 1; t <= k; t++) {
            int maxDiff = -prices[0];  // Maximum profit difference (profit after buying on day 0)
            for (int i = 1; i < n; i++) {
                dp[t][i] = Math.max(dp[t][i - 1], prices[i] + maxDiff);
                maxDiff = Math.max(maxDiff, dp[t - 1][i] - prices[i]);
            }
        }
        
        return dp[k][n - 1];  // Maximum profit with at most k transactions on the last day
    }
}